#!/bin/bash
# Batch-generate 30 thinking courses via the teaching agent API (port 8082)
# Usage: bash batch_generate.sh [--stage 1|2|3] [--dry-run]
set -e
BASE="http://localhost:8082/api/teaching/generate"
DRY_RUN=false
STAGE=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --dry-run) DRY_RUN=true; shift ;;
    --stage) STAGE="$1"; shift 2 ;;
    *) echo "Unknown: $1"; exit 1 ;;
  esac
done

generate() {
  local age="$1" topic="$2" total="$3" idx="$4"
  echo ">>> [$idx/$total] [$age] $topic"
  if $DRY_RUN; then return 0; fi
  local resp
  resp=$(curl -s -X POST "$BASE" \
    -H "Content-Type: application/json" \
    -d "{\"topic\": \"$topic\", \"age\": \"$age\"}")
  echo "$resp" | python3 -c "
import sys,json
d=json.load(sys.stdin)
if d.get('success'):
    print(f'  ✅ {d[\"course_name\"]} ({d[\"chapters\"]} chapters)')
else:
    print(f'  ❌ {d.get(\"error\",\"unknown\")}')"
  echo ""
}

# ── Stage 1: 4-6岁 具象游戏 ──
S1=(

"小动物排排队｜一一对应与数感：把一个苹果拖给一只小熊，一个香蕉拖给一只猴子，理解数量与实物的对应"
"糖果色风暴｜单维分类：玩具倒了一地，先按颜色把红色的放一框，再按形状把圆形的放一框"
"啪嗒啪嗒跳个舞｜视觉模式识别：踏脚拍手踏脚拍手，红魔方黄魔方红魔方，选出下一个"
"影子连连看｜平面空间感知：通过轮廓把小狗小猫和它们的影子连线，培养图形匹配"
"变多还是变少｜数量守恒：两排一样多的糖果，拉长一排后是不是变多了？动手数一数"
"小树长高了｜比较与序列：三棵高矮不同的小树，按从矮到高贴123标签"
"找出捣蛋鬼｜基础排除法：4只大象里混进1只兔子，找出和大家不一样的那一个"
"神奇魔法袋｜触觉与形状联想：摸到三角尖尖硬硬的东西，猜是什么形状"
"分一分大西瓜｜二等分感知：画一条线把西瓜切成一样大的两半，建立一半的概念"
"小老鼠走迷宫｜方向与路径：点击上下左右箭头，指挥小老鼠避开小猫吃到奶酪"

)

# ── Stage 2: 7-8岁 故事线索 ──
S2=(

"密码破译墙｜符号化思维：星星加星星等于6，一个星星是多少？学习等量代换"
"神秘服装店的订单｜多维分类：找一件红色且带口袋的衣服，用韦恩图思想"
"消失的第100个方块｜周期规律：红蓝绿循环的彩灯，第20个是什么颜色？用除法余数思考"
"谁偷了魔法药水｜逻辑演绎：三个嫌疑人各说一句话，用表格列线索推理谁说谎"
"城堡后面的隐形方块｜3D空间想象：积木堆叠，大白熊脚下藏了几个看不见的积木"
"怪兽镜子大冒险｜镜像轴对称：镜子里的影子举哪只手？剪纸打开是什么样"
"天平两端的小秘密｜逆向思维：苹果换橘子，菠萝换苹果，菠萝能换几个橘子"
"时间小管家的金币｜统筹规划：写作业20分钟烧水10分钟听英语10分钟，怎么安排用时最少"
"火柴棒变变变｜空间重构：移动一根火柴把6变成0，培养发散思维"
"小小社区统计员｜数据图表：涂格子做柱状图，哪个动物喜欢的食物最多"

)

# ── Stage 3: 9-10岁 策略PK ──
S3=(

"数字迷宫与最佳路径｜图论优化：外卖员去4个地方送货，规划总用时最短的路径"
"抢30的必胜法宝｜博弈论：轮流数1或2个数，谁先到30谁赢，倒推必胜策略点"
"鸡兔同笼的N种解法｜多角度建模：假设法列表法图形法，理解多种思维模型"
"折纸能触及月球吗｜指数爆炸：0.1毫米的纸对折30次有多厚？打破线性思维"
"不可能的裁剪｜拓扑思维：A4纸中间剪洞，怎么让整个人穿过去"
"狼羊菜的现代版危机｜状态机：三件危险品两两相克，建立状态转移图推导过河步骤"
"假币风波｜二分法：8个金币1个假的，用没刻度的天平最少称几次保证找出"
"天气预报背后的秘密｜概率思维：降水概率80%不一定下雨，理解概率是风险评估"
"工程队的效率大战｜归一问题：A队3天90米B队2天80米，一起修260米要几天"
"逻辑悖论说谎的长鼻子｜批判性思维：我说的这句话是假话，到底真还是假"

)

if [[ -z "$STAGE" || "$STAGE" == "1" ]]; then
echo "══════════════════════════════════════════════════"
echo "  阶段一：4-6岁 启蒙期 — 具象游戏与感官思维"
echo "══════════════════════════════════════════════════"
total=${#S1[@]}
i=1
for t in "${S1[@]}"; do generate "4-6" "$t" "$total" "$i"; ((i++)); done
fi

if [[ -z "$STAGE" || "$STAGE" == "2" ]]; then
echo "══════════════════════════════════════════════════"
echo "  阶段二：7-8岁 过渡期 — 故事线索与形象逻辑"
echo "══════════════════════════════════════════════════"
total=${#S2[@]}
i=1
for t in "${S2[@]}"; do generate "7-8" "$t" "$total" "$i"; ((i++)); done
fi

if [[ -z "$STAGE" || "$STAGE" == "3" ]]; then
echo "══════════════════════════════════════════════════"
echo "  阶段三：9-10岁 跃迁期 — 智力PK与抽象策略"
echo "══════════════════════════════════════════════════"
total=${#S3[@]}
i=1
for t in "${S3[@]}"; do generate "9-10" "$t" "$total" "$i"; ((i++)); done
fi

echo "🎉 Done!"
